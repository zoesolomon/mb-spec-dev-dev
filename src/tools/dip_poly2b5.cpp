void evpoly(const double* w1, const double* w2, 
	    const double* xH, const double* xO, 
	    double* px, double* py, double* pz)
{
    const double* Oa  = w1;
    const double* Ha1 = w1 + 3;
    const double* Ha2 = w1 + 6;

    const double* Ob  = w2;
    const double* Hb1 = w2 + 3;
    const double* Hb2 = w2 + 6;

    const size_t ncH = h2o::dip_poly2b5::ncoeffs_H;
    const size_t ncO = h2o::dip_poly2b5::ncoeffs_O;

    double pOa[ncO], pHa1[ncH], pHa2[ncH];
    double pOb[ncO], pHb1[ncH], pHb2[ncH];

    poly::cpt_poly2b5(xH, xO,
		      pHa1, pHa2, pHb1, pHb2,
 		      pOa, pOb);

    for(size_t i = 0; i < ncH; ++i){
	px[i] = pHa1[i]*Ha1[0] + pHa2[i]*Ha2[0]
  	      + pHb1[i]*Hb1[0] + pHb2[i]*Hb2[0];
	py[i] = pHa1[i]*Ha1[1] + pHa2[i]*Ha2[1]
  	      + pHb1[i]*Hb1[1] + pHb2[i]*Hb2[1];
	pz[i] = pHa1[i]*Ha1[2] + pHa2[i]*Ha2[2]
  	      + pHb1[i]*Hb1[2] + pHb2[i]*Hb2[2];
    }

    for(size_t i = 0; i < ncO; ++i){
	const size_t j = ncH + i;
	px[j] = pOa[i]*Oa[0] + pOb[i]*Ob[0];
	py[j] = pOa[i]*Oa[1] + pOb[i]*Ob[1];
	pz[j] = pOa[i]*Oa[2] + pOb[i]*Ob[2];
    }

}

double morse(const double& k, const double& r0,
             const double* a1, const double* a2)
{
    const double x = k*(tools::distance(a1, a2) - r0);
    return std::exp(-x);
}

