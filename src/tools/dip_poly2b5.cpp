void dip_poly2b5::d(const double* inp_w1, const double* inp_w2, 
		    double* poly_x, double* poly_y, double* poly_z)
{
    const size_t natm = 3;

    double w1[3*natm];
    double w2[3*natm];
    // center molecules wrt average O-O position
    {
	std::copy(inp_w1, inp_w1 + 3*natm, w1);
	std::copy(inp_w2, inp_w2 + 3*natm, w2);

	double center[3] = { (w1[0] + w2[0]) / 2.0,
	                     (w1[1] + w2[1]) / 2.0,
	                     (w1[2] + w2[2]) / 2.0 };
	for(size_t i = 0; i < natm; ++i){
	    for(size_t j = 0; j < 3; ++j){
		w1[3*i + j] -= center[j];
		w2[3*i + j] -= center[j];
	    }
	}
    }

    const double* Oa  = w1;
    const double* Ha1 = w1 + 3;
    const double* Ha2 = w1 + 6;

    const double* Ob  = w2;
    const double* Hb1 = w2 + 3;
    const double* Hb2 = w2 + 6;

    double xH[15], xO[15];

    xH[0]  = morse(kH_HH, r0_HHi, Ha1, Ha2);
    xH[1]  = morse(kH_HH, r0_HH , Ha1, Hb1);
    xH[2]  = morse(kH_HH, r0_HH , Ha1, Hb2);
    xH[3]  = morse(kH_HH, r0_HH , Ha2, Hb1);
    xH[4]  = morse(kH_HH, r0_HH , Ha2, Hb2);
    xH[5]  = morse(kH_HH, r0_HHi, Hb1, Hb2);
    xH[6]  = morse(kH_OH, r0_OHi, Oa , Ha1);
    xH[7]  = morse(kH_OH, r0_OHi, Oa , Ha2);
    xH[8]  = morse(kH_OH, r0_OH , Oa , Hb1);
    xH[9]  = morse(kH_OH, r0_OH , Oa , Hb2);
    xH[10] = morse(kH_OH, r0_OH , Ob , Ha1);
    xH[11] = morse(kH_OH, r0_OH , Ob , Ha2);
    xH[12] = morse(kH_OH, r0_OHi, Ob , Hb1);
    xH[13] = morse(kH_OH, r0_OHi, Ob , Hb2);
    xH[14] = morse(kH_OO, r0_OO , Oa , Ob );

    xO[0]  = morse(kO_HH, r0_HHi, Ha1, Ha2);
    xO[1]  = morse(kO_HH, r0_HH , Ha1, Hb1);
    xO[2]  = morse(kO_HH, r0_HH , Ha1, Hb2);
    xO[3]  = morse(kO_HH, r0_HH , Ha2, Hb1);
    xO[4]  = morse(kO_HH, r0_HH , Ha2, Hb2);
    xO[5]  = morse(kO_HH, r0_HHi, Hb1, Hb2);
    xO[6]  = morse(kO_OH, r0_OHi, Oa , Ha1);
    xO[7]  = morse(kO_OH, r0_OHi, Oa , Ha2);
    xO[8]  = morse(kO_OH, r0_OH , Oa , Hb1);
    xO[9]  = morse(kO_OH, r0_OH , Oa , Hb2);
    xO[10] = morse(kO_OH, r0_OH , Ob , Ha1);
    xO[11] = morse(kO_OH, r0_OH , Ob , Ha2);
    xO[12] = morse(kO_OH, r0_OHi, Ob , Hb1);
    xO[13] = morse(kO_OH, r0_OHi, Ob , Hb2);
    xO[14] = morse(kO_OO, r0_OO , Oa , Ob );

    evpoly(w1, w2, xH, xO, poly_x, poly_y, poly_z);

}

void dip_poly2b5::eval
    (const double* w1, const double* w2, double* dip)
{
    double poly_x[ncoeffs_H + ncoeffs_O];
    double poly_y[ncoeffs_H + ncoeffs_O];
    double poly_z[ncoeffs_H + ncoeffs_O];

    d(w1, w2, poly_x, poly_y, poly_z);

    double accum_x(0), accum_y(0), accum_z(0);
    for (size_t n = 0; n < ncoeffs; ++n){
        accum_x += thefit[n]*poly_x[n];
        accum_y += thefit[n]*poly_y[n];
        accum_z += thefit[n]*poly_z[n];
    }

    dip[0] = accum_x;
    dip[1] = accum_y;
    dip[2] = accum_z;

}

